# Crie um programa que solicite ao usuário dois números e exiba a soma, subtração, multiplicação e divisão entre eles

numero1 = int(input('digite um numero\n'))
numero2 = int(input('digite mais um numero\n'))
soma = numero1 + numero2
subtração = numero1 - numero2
multiplicação = numero1 * numero2
divisão = numero1 / numero2
print(f'a soma {numero1} + {numero2} é igual {soma}')
print(f'a subtração {numero1} + {numero2} é igual {subtração}')
print(f'a multiplicação {numero1} + {numero2} é igual {multiplicação}')
print(f'a divisão {numero1} + {numero2} é igual {divisão}')