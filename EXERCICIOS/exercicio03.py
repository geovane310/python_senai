# . Desenvolva um programa que pergunte ao usuário o dia da semana (número de 1 a 7) e exiba o nome do dia correspondente

dia = int(input('informe o dia da semana de 1 a 7 \n'))
if dia == 1:
      print('domingo')
elif dia == 2:
      print('segunda')    
elif dia == 3:
      print('terça') 
elif dia == 4:
      print('quarta')
elif dia == 5:
      print('quinta')
elif dia == 6:
      print('sexta')
elif dia == 7:
      print('sabado')
else:
      print('dia inesistente')                                     